# Modos:
# "r" - Ler - Valor padrão. Abre um arquivo para leitura, erro de o arquivo não existe.
# "a" - Anexar - aber um arquivo para anexar, cria o arquivo se ele não existir.
# "w" - Gravas - abre um arquivo para gravação, cria o arquivo se ele não existir.
# "x" - Criar - cria o arquivo especificado, retorna um erro se o arquivo ja existir.

# Tipos:
# "t" - Texto - valor padrão. Modo Texto
# "b" - Binário - Modo binário (por exemplo, imagens)